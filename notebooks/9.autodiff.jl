# ╔═╡ 53c29044-94d3-4470-bacb-e72bd2401d1c
md"## Poorman's Bessel function"

# ╔═╡ 8da8ed7e-bfd2-4be0-a764-ad04566b9632
function poor_besselj(ν, z::T; atol=eps(T)) where T
    k = 0
    s = (z/2)^ν / factorial(ν)
    out = s
    while abs(s) > atol
        k += 1
        s *= (-1) / k / (k+ν) * (z/2)^2
        out += s
    end
    out
end

