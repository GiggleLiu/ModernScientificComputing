md"## Newton's Method"

# ╔═╡ c6ad7442-d6df-4e29-a55c-ce0c1b4ec1cc
function Newton1d(f, x; tol=1e-5)
	k = 0
	while k < 1000
		k += 1
		dx = ForwardDiff.gradient(v->f(v[1]), [x])[1]/ForwardDiff.hessian(v->f(v[1]), [x])[1]
		x -= dx
		abs(dx) < tol && break
	end
	@info "number of iterations = $k"
	return x
end

