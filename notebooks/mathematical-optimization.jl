md"## Golden section search"

# ╔═╡ 738227f7-b9a8-4b20-b0a1-ce3664cb7138
function golden_section_search(f, a, b; tol=1e-5)
	τ = (√5 - 1) / 2
	x1 = a + (1 - τ) * (b - a)
	x2 = a + τ * (b - a)
	f1, f2 = f(x1), f(x2)
	k = 0
	while b - a > tol
		k += 1
		if f1 > f2
			a = x1
			x1 = x2
			f1 = f2
			x2 = a + τ * (b - a)
			f2 = f(x2)
		else
			b = x2
			x2 = x1
			f2 = f1
			x1 = a + (1 - τ) * (b - a)
			f1 = f(x1)
		end
	end
	@info "number of iterations = $k"
	return f1 < f2 ? (a, f1) : (b, f2)
end

